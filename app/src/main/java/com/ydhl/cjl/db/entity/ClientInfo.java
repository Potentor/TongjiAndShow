package com.ydhl.cjl.db.entity;

/**
 * 客户信息
 */
public class ClientInfo {
    /**客户姓名*/
    private String c_name;
    /**客户手机号*/
    private String c_phoneNum;
    /**客户性别*/
    private String c_sex;
    /**客户邮箱*/
    private String c_email;
    /**客户所属部门*/
    private String c_sector;
    /**客户业务*/
    private String c_job;
    /**客户地址*/
    private String c_address;
    /**客户网站*/
    private String c_net;
    /**客户qq*/
    private String c_qq;
    /**客户团队*/
    private String c_team;

    public String getC_name() {
        return c_name;
    }

    public void setC_name(String c_name) {
        this.c_name = c_name;
    }

    public String getC_phoneNum() {
        return c_phoneNum;
    }

    public void setC_phoneNum(String c_phoneNum) {
        this.c_phoneNum = c_phoneNum;
    }

    public String getC_sex() {
        return c_sex;
    }

    public void setC_sex(String c_sex) {
        this.c_sex = c_sex;
    }

    public String getC_email() {
        return c_email;
    }

    public void setC_email(String c_email) {
        this.c_email = c_email;
    }

    public String getC_sector() {
        return c_sector;
    }

    public void setC_sector(String c_sector) {
        this.c_sector = c_sector;
    }

    public String getC_job() {
        return c_job;
    }

    public void setC_job(String c_job) {
        this.c_job = c_job;
    }

    public String getC_address() {
        return c_address;
    }

    public void setC_address(String c_address) {
        this.c_address = c_address;
    }

    public String getC_net() {
        return c_net;
    }

    public void setC_net(String c_net) {
        this.c_net = c_net;
    }

    public String getC_qq() {
        return c_qq;
    }

    public void setC_qq(String c_qq) {
        this.c_qq = c_qq;
    }

    public String getC_team() {
        return c_team;
    }

    public void setC_team(String c_team) {
        this.c_team = c_team;
    }
}
